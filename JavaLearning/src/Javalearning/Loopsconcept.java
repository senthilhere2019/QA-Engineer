 package Javalearning;

public class Loopsconcept {

	public static void main(String[] args) {
		
	int i =1;
	while(i<10) {
		System.out.println(i);
	     i =i+2;
}
	
	System.out.println("*********************");
	for(int k=0;k<=10;k++) {
		System.out.println(k);
	}
	
	System.out.println("---------------------");
	for(int j=10;j>=2;j--) {
		System.out.println(j);
	}
	}

}
